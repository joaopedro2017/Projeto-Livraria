    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        String cpf = jftfCPF.getText().replace(".", "").replace("-", "");
        
        if(!cpf.trim().equals("")){
            Cliente cliente = FormPrincipal.bdCliente.buscarCliente(cpf);
            if(cliente != null){
                buscarDadosCliente(cliente);
                
            }else{
                JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                this.jbLimparActionPerformed(evt);
            }       
        }else{
            JOptionPane.showMessageDialog(null, "Digite corretamente o CPF!");
            jftfCPF.requestFocus();        
        }       
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void buscarDadosCliente(Cliente cliente) {
        jtfNome.setText(cliente.getNome());
        jftfTelefone.setText(cliente.getTelefone());
        jtfEmail.setText(cliente.getEmail());
        
        //Codigo para mostrar o status do RADIO
        JRadioButton radio;
        String status = cliente.getEstadoCivil();
        Enumeration jr = buttonGroupEstadoCivil.getElements();
        while( jr.hasMoreElements() ){
            radio = (JRadioButton) jr.nextElement();
            if(radio.getText().equals(status))
                radio.setSelected(true);
        }
        //FIM
        jtfLogradouro.setText(cliente.getEndereco().getLogradouro() );
        jtfComplemento.setText(cliente.getEndereco().getComplemento() );
        jtfCidade.setText(cliente.getEndereco().getCidade() );
        jftfCEP.setText(cliente.getEndereco().getCep() );
        
        //Trazer o dado para tela no ComboBox
        jcbEstado.setSelectedItem( cliente.getEndereco().getEstado() );
        //Ativando Atualizar
        jbAtualizar.setEnabled(true);
        jftfCPF.setEditable(false);
        jftfCPF.setBackground(Color.gray);
        jbCadastrar.setEnabled(false);
    }

