    private void jbAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAtualizarActionPerformed
        String cpf = jftfCPF.getText().replace(".", "").replace("-", "");
        
        Object[] options = {"Sim", "Não"}; 
        int decisao = JOptionPane.showOptionDialog(null, "Tem certeza que deseja Atualizar?", "ATENÇÃO", 
        JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]); 
        
        if (decisao == JOptionPane.YES_OPTION){        
            Cliente cli = FormPrincipal.bdCliente.buscarCliente(cpf);        
            preencherDadosDoCliente(cli, cpf);        
            FormPrincipal.bdCliente.alterarCliente(cli);
            JOptionPane.showMessageDialog(null, "Atualizado com sucesso", "Confirmação", JOptionPane.INFORMATION_MESSAGE);
            jbCadastrar.setEnabled(true); 
            jftfCPF.setBackground(Color.white);
            jftfCPF.setEditable(true);
            jbAtualizar.setEnabled(false);
            jbLimparActionPerformed(evt);            
        }
    }//GEN-LAST:event_jbAtualizarActionPerformed

    private void preencherDadosDoCliente(Cliente cli, String cpf) {
        cli.getEndereco().setCep( jftfCEP.getText().replace(".", "").replace("-", "") );
        cli.getEndereco().setCidade(jtfCidade.getText());
        cli.getEndereco().setComplemento(jtfComplemento.getText());
        cli.getEndereco().setEstado( jcbEstado.getSelectedItem().toString() );
        cli.getEndereco().setLogradouro(jtfLogradouro.getText() );
        
        cli.setCPF( cpf );
        cli.setEmail(jtfEmail.getText()  );
        
        JRadioButton radio;
        String str = null;
        Enumeration jr = buttonGroupEstadoCivil.getElements();
        while ( jr.hasMoreElements() ){
            radio = (JRadioButton) jr.nextElement();
            if (radio.isSelected())
                str = radio.getText();
        }
        cli.setEstadoCivil(str);
        cli.setNome( jtfNome.getText());
        cli.setTelefone(jftfTelefone.getText());
    }

    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        String cpf = jftfCPF.getText().replace(".", "").replace("-", "");        
        Cliente cliBuscar = FormPrincipal.bdCliente.buscarCliente(cpf);        
        
        if( !cpf.trim().equals("")  ){ 
                if( FormPrincipal.bdCliente.todosClientes().isEmpty()   ||  cliBuscar == null   ){
                    Cliente cli = new Cliente();

                    preencherDadosDoCliente(cli, cpf);                

                    FormPrincipal.bdCliente.adicionarCliente(cli);

                    JOptionPane.showMessageDialog(null, "Cliente cadastrado com Sucesso!", "Informação de Cadastro", JOptionPane.PLAIN_MESSAGE);
                    this.jbLimparActionPerformed(evt);
            }else{
                JOptionPane.showMessageDialog(null, "CPF já cadastrado!!!", "Atenção!", JOptionPane.WARNING_MESSAGE);        
            }        
        }else{
            JOptionPane.showMessageDialog(null, "Confira seu CFP!", "Messagem de ERRO", JOptionPane.INFORMATION_MESSAGE);
            jftfCEP.requestFocus();
        }       
    }//GEN-LAST:event_jbCadastrarActionPerformed

